//trigonometricas
export function seno(angulo:number):number {
    return Math.sin(angulo);
}

export function cosseno(angulo:number):number {
    return Math.cos(angulo)
}

export function tangente(angulo:number):number {
    return Math.tan(angulo)
}

export function senoHiperb(angulo:number):number {
    return Math.sinh(angulo);
}

export function cossenoHiperb(angulo:number):number {
    return Math.cosh(angulo);
}

export function tangenteHiperb(angulo:number):number {
    return Math.tanh(angulo);
}